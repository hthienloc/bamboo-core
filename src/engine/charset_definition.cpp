[[nodiscard]] const MappingView* findMapping(const DefinitionView& definition, char32_t codePoint) noexcept {
    const auto* begin = definition.mappings;
    const auto* end = definition.mappings + definition.size;
    const auto* it = std::lower_bound(begin, end, codePoint, [](const MappingView& mapping, char32_t value) {
        return mapping.source < value;
    });
    if (it != end && it->source == codePoint) {
        return it;
    }
    return nullptr;
}

void appendUtf8(std::string& output, char32_t codePoint) {
    if (codePoint <= 0x7F) { output.push_back(static_cast<char>(codePoint)); return; }
    if (codePoint <= 0x7FF) {
        output.push_back(static_cast<char>(0xC0 | (codePoint >> 6)));
        output.push_back(static_cast<char>(0x80 | (codePoint & 0x3F)));
        return;
    }
    if (codePoint <= 0xFFFF) {
        output.push_back(static_cast<char>(0xE0 | (codePoint >> 12)));
        output.push_back(static_cast<char>(0x80 | ((codePoint >> 6) & 0x3F)));
        output.push_back(static_cast<char>(0x80 | (codePoint & 0x3F)));
        return;
    }
    output.push_back(static_cast<char>(0xF0 | (codePoint >> 18)));
    output.push_back(static_cast<char>(0x80 | ((codePoint >> 12) & 0x3F)));
    output.push_back(static_cast<char>(0x80 | ((codePoint >> 6) & 0x3F)));
    output.push_back(static_cast<char>(0x80 | (codePoint & 0x3F)));
}

