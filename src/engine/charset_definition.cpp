const CharsetDefinition::DefinitionView* CharsetDefinition::find(std::string_view name) noexcept {
    for (const auto& definition : kDefinitions) {
        if (definition.name == name) {
            return &definition;
        }
    }
    return nullptr;
}

std::string_view CharsetDefinition::lookupEncoded(std::string_view name, char32_t codePoint) noexcept {
    const DefinitionView* definition = find(name);
    if (definition == nullptr) {
        return {};
    }
    const MappingView* mapping = findMapping(*definition, codePoint);
    return mapping == nullptr ? std::string_view{} : mapping->encoded;
}

std::string CharsetDefinition::encode(std::string_view name, std::u32string_view input) {
    std::string output;
    output.reserve(input.size() * 4U);
    const DefinitionView* definition = (name == kUnicode) ? nullptr : find(name);
    for (const char32_t codePoint : input) {
        if (definition != nullptr) {
            if (const MappingView* mapping = findMapping(*definition, codePoint); mapping != nullptr) {
                output.append(mapping->encoded.data(), mapping->encoded.size());
                continue;
            }
        }
        appendUtf8(output, codePoint);
    }
    return output;
}

const std::array<std::string_view, 17>& CharsetDefinition::names() noexcept {
    return kNames;
}

